package com.billing.command;

public class Consumption
{
	private String units;
	private String type;
	
	public String getUnits()
	{
		return units;
	}
	public void setUnits(String units) {
		this.units = units;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
}